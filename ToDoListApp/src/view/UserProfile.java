    private void userTaskActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_userTaskActionPerformed
      try {
            Registry registry = LocateRegistry.getRegistry("127.0.0.1", 1000);
            TasksInterface taskService = (TasksInterface) registry.lookup("task");
            List<Tasks> taskList = taskService.retrieveAll(new Tasks(utils.Session.CURRENT_USER_ID));
            if (!taskList.isEmpty()) {
                UserTask userTask = new UserTask();
                userTask.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "No tasks found");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Unable to connect to server. Please try again later.");
            e.printStackTrace();
        }
    }//GEN-LAST:event_userTaskActionPerformed

