    private void deleteUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteUserActionPerformed
        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Are you sure you want to delete " + utils.Session.CURRENT_USER_NAME + "? This will also delete all your tasks and categories.",
            "Delete Confirmation",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm == JOptionPane.YES_OPTION) {
            try {
                Registry registry = LocateRegistry.getRegistry("127.0.0.1", 1000);
                UserInterface userService = (UserInterface) registry.lookup("user");
                User user = new User();
                user.setId(utils.Session.CURRENT_USER_ID);
                String result = userService.deleteUser(user);
                if (result.equals("Success")) {
                    JOptionPane.showMessageDialog(this, "Account deleted successfully!");
                    utils.Session.CURRENT_USER_ID = 0;
                    utils.Session.CURRENT_USER_NAME = null;
                    utils.Session.CURRENT_USER_EMAIL = null;
                    utils.Session.CURRENT_USER_CREATED_AT = null;
                    new Signin().setVisible(true);
                    this.dispose();
                } else {
                    JOptionPane.showMessageDialog(this, "Failed to delete account: " + result);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Unable to connect to server. Please try again later.");
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_deleteUserActionPerformed

